// Defines the structure of a form for user input
export interface IForm {
    name: string;           // User's name
    birthday: string;      // User's birth date
    urlImage: string;      // URL of the user's image
    gender: string;        // User's gender
    clan: string;          // User's clan
    softSkill: string;     // User's soft skill
    language: string;      // User's language proficiency
    techSkill: string;     // User's technical skill
    description: string;   // Description or additional information about the user
}
